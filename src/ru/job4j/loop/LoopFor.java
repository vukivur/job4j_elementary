package ru.job4j.loop;

public class LoopFor {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        System.out.println();
        System.out.println("Numbers from 5 to 10:");
        for (int i = 5; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
